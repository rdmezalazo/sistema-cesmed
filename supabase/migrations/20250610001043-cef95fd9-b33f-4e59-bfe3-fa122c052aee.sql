-- Habilitar RLS para la tabla de enfermedades
ALTER TABLE public.enfermedades ENABLE ROW LEVEL SECURITY;

-- Política para permitir lectura a todos los usuarios autenticados
CREATE POLICY "Todos pueden ver enfermedades" 
  ON public.enfermedades 
  FOR SELECT 
  USING (true);

-- Política para permitir inserción solo a administradores
CREATE POLICY "Solo administradores pueden agregar enfermedades" 
  ON public.enfermedades 
  FOR INSERT 
  WITH CHECK (public.is_admin());

-- Política para permitir actualización solo a administradores  
CREATE POLICY "Solo administradores pueden actualizar enfermedades" 
  ON public.enfermedades 
  FOR UPDATE 
  USING (public.is_admin());

-- Política para permitir eliminación solo a administradores
CREATE POLICY "Solo administradores pueden eliminar enfermedades" 
  ON public.enfermedades 
  FOR DELETE 
  USING (public.is_admin());
