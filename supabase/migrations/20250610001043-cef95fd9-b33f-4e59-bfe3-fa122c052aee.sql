
-- Crear tabla de enfermedades con clasificación CIE10
CREATE TABLE public.enfermedades (
  id UUID NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
  cie10_clase TEXT NOT NULL,
  clase TEXT NOT NULL,
  cie10_enfermedad TEXT NOT NULL,
  nombre_enfermedad TEXT NOT NULL,
  vista_cie10 TEXT NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),
  updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now()
);

-- Crear índice para búsquedas rápidas
CREATE INDEX idx_enfermedades_vista_cie10 ON public.enfermedades USING gin(to_tsvector('spanish', vista_cie10));
CREATE INDEX idx_enfermedades_cie10 ON public.enfermedades (cie10_enfermedad);

