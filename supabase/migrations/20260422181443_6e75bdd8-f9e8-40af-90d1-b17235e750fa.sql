ALTER TABLE public.pharmacy_outputs
ADD COLUMN IF NOT EXISTS archivado BOOLEAN NOT NULL DEFAULT false,
ADD COLUMN IF NOT EXISTS fecha_archivo TIMESTAMP WITH TIME ZONE;

CREATE INDEX IF NOT EXISTS idx_pharmacy_outputs_active_medication_id
ON public.pharmacy_outputs (medication_id)
WHERE COALESCE(archivado, false) = false;

UPDATE public.pharmacy_medications
SET archivo_entrada = COALESCE(archivo_entrada, 0) + COALESCE(entrada, 0),
    archivo_salida = COALESCE(archivo_salida, 0) + COALESCE(salida, 0),
    fecha_archivo = now()
WHERE COALESCE(entrada, 0) <> 0 OR COALESCE(salida, 0) <> 0;

UPDATE public.pharmacy_outputs
SET archivado = true,
    fecha_archivo = COALESCE(fecha_archivo, now())
WHERE COALESCE(archivado, false) = false;

CREATE OR REPLACE FUNCTION public.recalculate_medication_stock(p_medication_id UUID)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_total_entrada INTEGER;
  v_total_salida INTEGER;
  v_stock_inicial INTEGER;
  v_stock_actual INTEGER;
BEGIN
  SELECT COALESCE(stock_inicial, 0)
  INTO v_stock_inicial
  FROM public.pharmacy_medications
  WHERE id = p_medication_id;

  IF NOT FOUND THEN
    RETURN;
  END IF;

  SELECT COALESCE(SUM(quantity_received), 0)
  INTO v_total_entrada
  FROM public.pharmacy_entries
  WHERE medication_id = p_medication_id
    AND COALESCE(archivado, false) = false;

  SELECT COALESCE(SUM(quantity), 0)
  INTO v_total_salida
  FROM public.pharmacy_outputs
  WHERE medication_id = p_medication_id
    AND COALESCE(archivado, false) = false;

  v_stock_actual := v_stock_inicial + v_total_entrada - v_total_salida;

  UPDATE public.pharmacy_medications
  SET entrada = v_total_entrada,
      salida = v_total_salida,
      stock_actual = v_stock_actual,
      updated_at = NOW()
  WHERE id = p_medication_id;
END;
$$;

